use std::io::{Error, ErrorKind, Result};

use crate::message::{self, Compressor, Outbound};

/// Response to "get_accepted_state_summary".
#[derive(
    std::clone::Clone,
    std::cmp::Eq,
    std::cmp::Ord,
    std::cmp::PartialEq,
    std::cmp::PartialOrd,
    std::fmt::Debug,
    std::hash::Hash,
)]
pub struct Message {
    pub chain_id: crate::ids::Id,
    pub request_id: u32,
    pub summary_ids: Vec<crate::ids::Id>,
}

impl Message {
    pub fn create(
        chain_id: crate::ids::Id,
        request_id: u32,
        summary_ids: Vec<crate::ids::Id>,
    ) -> impl Outbound + Compressor {
        Self {
            chain_id,
            request_id,
            summary_ids,
        }
    }
}

/// ref. https://doc.rust-lang.org/std/string/trait.ToString.html
/// ref. https://doc.rust-lang.org/std/fmt/trait.Display.html
/// Use "Self.to_string()" to directly invoke this
impl std::fmt::Display for Message {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "msg accepted_state_summary")
    }
}

impl Outbound for Message {
    fn serialize_with_header(&self) -> Result<bytes::Bytes> {
        let type_id = message::TYPES
            .get("accepted_state_summary")
            .ok_or_else(|| Error::new(ErrorKind::InvalidInput, "unknown type name"))?;

        let packer = message::default_packer_with_header();
        packer.pack_byte(*type_id)?;
        packer.pack_bool(false)?; // compressible
        packer.pack_bytes(self.chain_id.as_ref())?;
        packer.pack_u32(self.request_id)?;
        packer.pack_u32(self.summary_ids.len() as u32)?;
        for id in self.summary_ids.iter() {
            packer.pack_bytes(id.as_ref())?;
        }

        Ok(packer.take_bytes())
    }
}

